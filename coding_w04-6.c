#include <stdio.h>

const int GLOBAL_RATE = 10; 

void calculate() {
    const int LOCAL_BONUS = 50; 
    printf("GLOBAL_RATE = %d\n", GLOBAL_RATE);
    printf("LOCAL_BONUS = %d\n", LOCAL_BONUS);
}

int main() {
    calculate();
    printf("GLOBAL_RATE = %d\n", GLOBAL_RATE);
    return 0;
}

//1. GLOBAL_RATE เป็นค่าคงที่ระดับ global: ประกาศนอกฟังก์ชัน, สามารถเข้าถึงได้จากทุกฟังก์ชันในไฟล์นั้น, มีอายุตลอดการทำงานของโปรแกรม
//LOCAL_BONUS เป็นค่าคงที่ระดับ local: ประกาศภายในฟังก์ชัน calculate(), สามารถเข้าถึงได้เฉพาะภายในฟังก์ชันที่ประกาศ, มีอายุเฉพาะเมื่อฟังก์ชันทำงาน
//2. รันโค้ดได้ปกติ
//3. เพราะ LOCAL_BONUS เป็นตัวแปร local ที่ประกาศภายในฟังก์ชัน calculate(), ตัวแปร local จะมีขอบเขต (scope) เฉพาะภายในบล็อกที่ประกาศเท่านั้น
//,เมื่อออกจากฟังก์ชัน calculate() แล้ว ตัวแปรนี้จะไม่สามารถเข้าถึงได้จากที่อื่น
//4.การทดลองนี้แสดงให้เห็นถึงความแตกต่างระหว่างค่าคงที่ระดับ global และ local:
//ค่าคงที่ระดับ global เหมาะสำหรับค่าที่ต้องใช้ทั่วทั้งโปรแกรม
//ค่าคงที่ระดับ local เหมาะสำหรับค่าที่ใช้เฉพาะในฟังก์ชันนั้นๆ
//การใช้ค่าคงที่ (const) ช่วยป้องกันการเปลี่ยนแปลงค่าที่ไม่ต้องการ
//การพยายามเปลี่ยนค่าคงที่หรือเข้าถึง local constant จากนอกขอบเขตจะทำให้เกิดข้อผิดพลาดในการคอมไพล์
//การเข้าใจขอบเขตของตัวแปร (scope) เป็นสิ่งสำคัญในการออกแบบโปรแกรม